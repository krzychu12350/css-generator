export function getRandomHue() {
  // Step 1: Generate a random index to select a hue category
  const randomHueIndex = Math.floor(Math.random() * tailwindColorsArray.length);
  const selectedHue = tailwindColorsArray[randomHueIndex];

  // Step 2: Generate a random index to select a specific color within the selected hue category
  const randomColorIndex = Math.floor(Math.random() * selectedHue.colors.length);
  const selectedColor = selectedHue.colors[randomColorIndex];

  // Step 3: Return the selected color
  return {
    name: selectedHue.name,
    color: selectedColor
  };
}

